"""
=====================================================================
bot/states/states.py
=====================================================================
ЗАЧЕМ ЭТОТ ФАЙЛ (для чайника):
FSM (Finite State Machine / конечный автомат) — это способ помнить,
"на каком шаге" находится пользователь в диалоге с ботом.

Например, пользователь выбрал тему "Деньги" → бот запоминает это
состояние → следующее текстовое сообщение пользователя бот поймёт
как "вопрос про деньги", а не как случайный текст.

aiogram 3 хранит состояния через FSMContext (обычно в Redis, см.
main.py, где мы подключаем RedisStorage).
=====================================================================
"""

from aiogram.fsm.state import State, StatesGroup


class OracleStates(StatesGroup):
    # Пользователь выбирает язык (первый запуск)
    choosing_language = State()
    # Пользователь в главном меню тем
    choosing_topic = State()
    # Пользователь в режиме "чата с оракулом" по конкретной теме
    chatting = State()


class AdminStates(StatesGroup):
    # Админ вводит текст для рассылки всем пользователям
    waiting_broadcast_text = State()
    # Админ вводит telegram_id пользователя для поиска
    waiting_user_search = State()
    # Админ вводит новый системный промпт для AI
    waiting_new_prompt = State()
